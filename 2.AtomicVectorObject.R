# Logical data type
v <- TRUE; x <- FALSE
print(class(v))
print(mode(x))
print(length(x))

##################################################################

# Numeric data type
v <- 23.5
print(class(v))

##################################################################

# Integer Data type
v <- 2L
print(class(v))

##################################################################

# Complex Data type
v <- 2+5i
print(class(v))

##################################################################

# Character data type
v <- "TRUE"
print(class(v))

##################################################################

# Raw data type
v <- charToRaw("Hello")
print(class(v))